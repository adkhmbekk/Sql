--7. DQL (Data Query Language) — используется для извлечения данных из базы данных. Основная команда — SELECT.
--Пример: 
SELECT * FROM Customers;

  -- DML (Data Manipulation Language) — используется для работы с данными внутри таблиц: добавление, изменение и удаление.
--Примеры: 
INSERT INTO Products (Name, Price) VALUES ('Молоко', 100);
UPDATE Products SET Price = 120 WHERE Name = 'Молоко';
DELETE FROM Products WHERE Name = 'Молоко';

   --DDL (Data Definition Language) — используется для создания и изменения структуры базы данных: таблиц, схем, индексов и т.д.
--Примеры: 
CREATE TABLE Students (ID INT, Name NVARCHAR(50));
ALTER TABLE Students ADD Age INT;
DROP TABLE Students;

  --DCL (Data Control Language) — используется для управления правами доступа к объектам базы данных.
--Примеры: 
GRANT SELECT ON Customers TO user1;
REVOKE INSERT ON Customers FROM user1;

  --TCL (Transaction Control Language) — управляет транзакциями. Позволяет выполнять несколько операций как одну логическую единицу.
--Примеры:
BEGIN TRANSACTION;
UPDATE Accounts SET Balance = Balance - 500 WHERE ID = 1;
UPDATE Accounts SET Balance = Balance + 500 WHERE ID = 2;
COMMIT;
--Если возникает ошибка, вместо COMMIT можно использовать ROLLBACK, чтобы отменить изменения.

