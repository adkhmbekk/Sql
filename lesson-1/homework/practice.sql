--1. Данные (Data) — это факты, значения или набор информации, которые могут быть зафиксированы и использованы для анализа или принятия решений. Примеры: числа, текст, даты.
   --База данных (Database) — это организованная коллекция данных, которую можно легко просматривать, управлять и обновлять. Обычно используется для хранения информации в цифровом виде.
   --Реляционная база данных (Relational Database) — это тип базы данных, в которой данные хранятся в виде связанных между собой таблиц. Каждая таблица состоит из строк и столбцов, и между таблицами могут быть установлены отношения.
   --Таблица (Table) — это структура в базе данных, предназначенная для хранения данных. Таблица состоит из строк (записей) и столбцов (полей), где каждый столбец соответствует определённому типу данных.

--2. Поддержка транзакций — надёжная работа с данными, включая возможность отката изменений при ошибках.
   --Безопасность — встроенные механизмы авторизации, шифрования и управления доступом.
   --Масштабируемость и производительность — оптимизация работы с большими объёмами данных.
   --Интеграция с другими сервисами Microsoft — например, Power BI, Excel, Azure.
   --Поддержка хранимых процедур и триггеров — для автоматизации бизнес-логики внутри базы. 
  
--3. Windows Authentication — используется учётная запись Windows пользователя, нет необходимости вводить логин и пароль отдельно.
   --SQL Server Authentication — используется имя пользователя и пароль, заданные в SQL Server, независимо от учетной записи Windows.

